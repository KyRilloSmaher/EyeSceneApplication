    public enum WritingStyle
    {
        Narrative,       // Focuses on storytelling and character development
        Expository,      // Aims to inform or explain a topic clearly
        Descriptive,     // Uses detailed descriptions to create vivid imagery
        Persuasive,      // Seeks to convince the reader of a particular viewpoint
        Analytical,      // Breaks down complex topics into understandable parts
        Creative,        // Emphasizes imagination and originality in writing
        Technical,       // Provides detailed instructions or explanations in a specific field
        Journalistic,    // Reports news and events in a factual manner
        Academic,        // Involves scholarly research and formal writing conventions
        Reflective       // Encourages personal reflection and introspection in writing

    }
}